/** OPERADORES DE ASIGNACION 
    | Operador      | Descripción                    | Ejemplo                  |
    |---------------|--------------------------------|--------------------------|
    | =             | Asignación                     | a = 10;                  |
    | +=            | Asignación de adición          | a += 5;  // a = a + 5;   |
    | -=            | Asignación de sustracción      | a -= 5;  // a = a - 5;   |
    | *=            | Asignación de multiplicación   | a *= 5;  // a = a * 5;   |
    | /=            | Asignación de división         | a /= 5;  // a = a / 5;   |
    | %=            | Asignación de módulo           | a %= 5;  // a = a % 5;   |
    | **=           | Asignación de exponenciación   | a **= 5; // a = a ** 5;  |
*/

/** Asignacion */
let a = 10;
console.log( a );

/** Asignación de adición */
a += 3; 
console.log( a );

/** Asignación de sustracción */
a -= 4;
console.log( a );

/** Asignación de multiplicación */
a *= 3;
console.log( a );

/** Asignación de división */
a /= 2;
console.log( a );

/** Asignación de módulo */
a %= 3; 
console.log( a );

/** Asignación de exponenciación */
a **= 3;
console.log( a );
