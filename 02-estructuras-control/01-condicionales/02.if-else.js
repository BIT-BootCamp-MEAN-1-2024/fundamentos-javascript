/** if-else: Se utiliza para ejecutar un bloque de código si una condición es verdadera, y otro bloque si la condición es falsa 
    Sintáxis:

    if( condicion ) {
        Sentencias que se ejecutarán si se cumple la condición
    }
    else {
        Sentencias que se ejecutarán si NO se cumple la condición
    }
*/
let age = 17;

if( age > 18 ) {
    console.log( 'Eres mayor de edad' );
}
else {
    console.log( 'Eres menor de edad' );
}