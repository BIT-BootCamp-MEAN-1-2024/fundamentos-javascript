/** Función de expresión
    Se definen asignando una expresión de función (o función anónima) a una variable o constante.
 */

// Definicion del cuerpo de función (o función anónima) expresión declarativa
const saludaTerricolas = function () {
    console.log( 'Hola terricolas!' );
}

saludaTerricolas();      // Invocación de una función definida

// Definicion del cuerpo de función (o función anónima) expresión declarativa
const saludaJavaScripters = () => {
    console.log( 'Hola JavaScripters!' );
}

saludaJavaScripters();      // Invocación de una función definida